class Product {
  String id;
  String name;
  String price;
  int quantityLeft;
  String description;
  String category;
  String imageUrl;

  Product({
    required this.id,
    required this.name,
    required this.price,
    required this.quantityLeft,
    required this.description,
    required this.category,
    required this.imageUrl,
  });
}
